//Write a program to print the first 10 odd natural numbers in reverse order.
#include<stdio.h>
int main ()
{
  int i=1;
  printf("First 10 Odd Natural Numbers in Reverse order:\n");
  while(i<=10)
  {
    printf("%d ",21-2*i);
    i++;
  }
  return 0;

}

